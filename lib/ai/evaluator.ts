import OpenAI from 'openai';

export interface QuickEvaluationInput {
  idea: string;
  problem: string;
  targetAudience: string;
  category?: string;
}

export interface DetailedEvaluationInput {
  projectName: string;
  idea: string;
  problem: string;
  solution: string;
  targetAudience: string;
  competitors: string;
  category?: string;
}

export interface EvaluationResult {
  score: number;
  strengths: string[];
  weaknesses: string[];
  opportunities: string[];
  risks: string[];
  recommendations: string[];
  marketAnalysis: string;
  financialProjection: string;
}

/**
 * Evaluate a quick idea using GPT-4 Turbo with comprehensive analysis
 */
export async function evaluateQuickIdea(input: QuickEvaluationInput): Promise<EvaluationResult> {
  const openai = new OpenAI({
    apiKey: process.env.OPENAI_API_KEY,
  });

  const systemPrompt = `أنت مستشار أعمال خبير متخصص في تقييم الأفكار الريادية في السوق السعودي. لديك خبرة 20+ سنة في:
- تحليل نماذج الأعمال وتقييم الجدوى
- دراسة السوق السعودي والخليجي
- التخطيط المالي والتوقعات الاستثمارية
- تحديد الفرص والمخاطر بدقة عالية

مهمتك: تقديم تقييم شامل ودقيق للأفكار الريادية بناءً على معايير احترافية.`;

  const userPrompt = `قيّم هذه الفكرة الريادية بشكل شامل ومهني:

**الفكرة:** ${input.idea}
**المشكلة التي تحلها:** ${input.problem}
**الجمهور المستهدف:** ${input.targetAudience}
${input.category ? `**التصنيف:** ${input.category}` : ''}

---

**المطلوب منك:**

1. **درجة التقييم الإجمالية (من 100):**
   - قيّم الفكرة بناءً على: الابتكار، حجم السوق، قابلية التنفيذ، الميزة التنافسية، والعائد المتوقع
   - كن واقعياً ودقيقاً في التقييم

2. **نقاط القوة (3-5 نقاط):**
   - حدد المزايا الحقيقية والقوية للفكرة
   - ركز على ما يميزها عن الحلول الموجودة
   - اذكر الفرص الكبيرة التي يمكن استغلالها

3. **نقاط الضعف (3-5 نقاط):**
   - كن صريحاً في تحديد التحديات والعقبات
   - اذكر المخاطر المحتملة والثغرات
   - حدد ما يحتاج إلى تحسين أو تطوير

4. **الفرص المتاحة (3-5 نقاط):**
   - حدد الفرص السوقية الحقيقية
   - اذكر إمكانيات التوسع والنمو
   - ركز على الاتجاهات السوقية المواتية

5. **المخاطر المحتملة (3-5 نقاط):**
   - حدد المخاطر التشغيلية والمالية
   - اذكر التحديات التنظيمية والقانونية
   - ركز على المنافسة والتغيرات السوقية

6. **التوصيات الاستراتيجية (3-5 نقاط):**
   - قدم نصائح عملية وقابلة للتنفيذ
   - اقترح خطوات واضحة للبدء
   - ركز على أولويات التطوير

7. **تحليل السوق (فقرة شاملة 100-150 كلمة):**
   - حلل حجم السوق السعودي المستهدف
   - اذكر الاتجاهات والفرص الحالية
   - قيّم المنافسة والطلب المتوقع
   - استخدم أرقام تقديرية واقعية

8. **التوقعات المالية (فقرة شاملة 100-150 كلمة):**
   - قدّر التكاليف الأولية المطلوبة
   - توقع الإيرادات المحتملة في السنة الأولى
   - اذكر نقطة التعادل المتوقعة
   - قيّم العائد على الاستثمار (ROI)

---

**مهم جداً:**
- يجب أن يكون الرد بصيغة JSON فقط
- استخدم اللغة العربية الفصحى المبسطة
- كن دقيقاً ومهنياً في التحليل
- قدم معلومات واقعية وقابلة للتطبيق

**الصيغة المطلوبة:**
\`\`\`json
{
  "score": 85,
  "strengths": [
    "نقطة قوة مفصلة ومحددة",
    "نقطة قوة أخرى مع تفاصيل",
    "نقطة قوة ثالثة"
  ],
  "weaknesses": [
    "نقطة ضعف واضحة مع شرح",
    "نقطة ضعف أخرى",
    "نقطة ضعف ثالثة"
  ],
  "opportunities": [
    "فرصة سوقية محددة",
    "فرصة نمو واضحة",
    "فرصة استراتيجية"
  ],
  "risks": [
    "مخاطرة محتملة مع تفاصيل",
    "مخاطرة تشغيلية",
    "مخاطرة سوقية"
  ],
  "recommendations": [
    "توصية عملية قابلة للتنفيذ",
    "توصية استراتيجية واضحة",
    "توصية تطويرية محددة"
  ],
  "marketAnalysis": "تحليل شامل للسوق السعودي يتضمن حجم السوق المستهدف، الاتجاهات الحالية، المنافسة، والطلب المتوقع مع أرقام تقديرية واقعية...",
  "financialProjection": "توقعات مالية واقعية تتضمن التكاليف الأولية المقدرة، الإيرادات المتوقعة في السنة الأولى، نقطة التعادل، والعائد على الاستثمار المتوقع..."
}
\`\`\``;

  try {
    const completion = await openai.chat.completions.create(
      {
        model: 'gpt-4-turbo',
        messages: [
          {
            role: 'system',
            content: systemPrompt,
          },
          {
            role: 'user',
            content: userPrompt,
          },
        ],
        temperature: 0.7,
        max_tokens: 3000,
        response_format: { type: 'json_object' },
      },
      {
        timeout: 50000,
      }
    );

    const content = completion.choices[0].message.content?.trim() || '{}';
    const result: EvaluationResult = JSON.parse(content);
    
    // Validate result structure
    if (!result.score || !result.strengths || !result.weaknesses) {
      throw new Error('Invalid response format from AI');
    }
    
    return result;
  } catch (error: any) {
    console.error('OpenAI API Error:', error);
    throw new Error(`فشل التقييم: ${error.message}`);
  }
}

/**
 * Evaluate a detailed idea using GPT-4 Turbo with comprehensive analysis
 */
