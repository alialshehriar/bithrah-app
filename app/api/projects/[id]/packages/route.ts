export async function GET(
  request: NextRequest,
  { params }: { params: { id: string } }
) {
  try {
    const projectId = parseInt(params.id);

    // Return hardcoded packages for demo projects
    if (DEMO_PACKAGES[projectId]) {
      return NextResponse.json({
        success: true,
        packages: DEMO_PACKAGES[projectId]
      });
    }

    // Fallback to database for other projects
    const packages = await db
      .select()
      .from(supportPackages)
      .where(eq(supportPackages.projectId, projectId))
      .orderBy(asc(supportPackages.amount));

    return NextResponse.json({
      success: true,
      packages
    });
  } catch (error) {
    console.error('Error fetching support packages:', error);
    return NextResponse.json(
      { success: false, error: 'Failed to fetch support packages' },
      { status: 500 }
    );
  }
}
